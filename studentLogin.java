    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String rollno = jTextField2.getText();
        String password = jPasswordField1.getText();
        
    
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","root");
            Statement st = con.createStatement();
            ResultSet rs1 = st.executeQuery("select * from student where rollno = '"+rollno+"' and pass = '"+password+"' ");

            if(rs1.next())
            {
                ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno = '"+rollno+"' and result.rollno = '"+rollno+"' ");
                    if(rs.next())
                    {
                                setVisible(false);
                new StudentHome(rollno).setVisible(true);
//          
                }
                    else
                    {
              JOptionPane.showMessageDialog(null, "Result Record Not Found");
                    }
            }
            else{
                 JOptionPane.showMessageDialog(null, "Roll No or Password is Incorrect");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

