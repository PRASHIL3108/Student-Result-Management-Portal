    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String  rollno = jTextField5.getText();
     
        // TODO add your handling code here:   
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from result where rollno ='"+rollno+"'");

               if(rs.next())
               {
                    st.executeUpdate("delete from result where rollno = '"+rollno+"' ");
                    JOptionPane.showMessageDialog(null,"Record Successfully Deleted");
                    setVisible(false);
                    new DeleteResultsDetails().setVisible(true);
               }
               else
               {
                    JOptionPane.showMessageDialog(null,"Roll_no Dose not Exist.");
                    setVisible(false);
                 new DeleteResultsDetails().setVisible(true);
               }
                 
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }  
       
    }//GEN-LAST:event_jButton5ActionPerformed

