    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String  username = jTextField5.getText();
        String  password = jPasswordField1.getText();
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from teachers_details where username='"+username+"' and password = '"+password+"'");
                if(rs.next())
                {
                    st.executeUpdate("delete from teachers_details  where username = '"+username+"' and password = '"+password+"' ");
                    JOptionPane.showMessageDialog(null,"Record Successfully Deleted");
                    setVisible(false);
                    new DeleteTeachersRecord().setVisible(true);
                }
                else
                {
                 JOptionPane.showMessageDialog(null, "Username or Password is Incorrect");
                  
                }
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }      
    }//GEN-LAST:event_jButton5ActionPerformed

