    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String  rollno = jTextField5.getText();
        String  pass = jPasswordField1.getText();
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno ='"+rollno+"' and pass = '"+pass+"'");

               if(rs.next())
               {
                    st.executeUpdate("delete from student where rollno = '"+rollno+"' and pass = '"+pass+"' ");
                    JOptionPane.showMessageDialog(null,"Record Successfully Deleted");
                    setVisible(false);
                    new DeleteStudentsDetails().setVisible(true);
               }
               else
               {
                    JOptionPane.showMessageDialog(null,"Roll_no or Password Dose not Exist.");
                     setVisible(false);
                 new DeleteStudentsDetails().setVisible(true);
               }
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        } 
    }//GEN-LAST:event_jButton5ActionPerformed

