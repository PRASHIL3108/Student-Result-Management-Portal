    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String  rollno = jTextField5.getText();
        String  DSA = jTextField1.getText();
        String  Front_End = jTextField2.getText();
        String  Back_End = jTextField3.getText();
        String  Java = jTextField6.getText();
        String  MySql = jTextField7.getText();
       
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next())
            {
                st.executeLargeUpdate("insert into result(rollno,dsa,front_end,back_end,java,mysql) values('"+rollno+"','"+DSA+"','"+Front_End+"','"+Back_End+"','"+Java+"','"+MySql+"')");
                JOptionPane.showMessageDialog(null,"Result Successfully Added");
                setVisible(false);
                new InsertNewResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Roll No Dose not Exist.");
            }
            
            setVisible(false);
            new InsertNewResult().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

